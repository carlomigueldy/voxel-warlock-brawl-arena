/* Workflow runtime globals injected by the Claude Code Workflow engine. */
/* global args, agent, parallel, phase, log */

export const meta = {
  name: "feature-implementation",
  description:
    "Opus plans a feature into disjoint file-scoped tasks → parallel Sonnet implementers build them → Opus adversarial review → Sonnet fix loop until clean",
  whenToUse:
    "Run to implement a scoped feature end-to-end. Pass the requirement as args (string) or args: { goal, base?, maxFixRounds? }. The plan partitions work into non-overlapping file scopes so implementers run in parallel safely.",
  phases: [
    { title: "Plan", model: "opus" },
    { title: "Implement" },
    { title: "Review", model: "opus" },
    { title: "Fix" },
  ],
};

// ── Parameters ────────────────────────────────────────────────────────────
const GOAL = (typeof args === "string" && args) || (args && args.goal) || "";
const BASE = (args && args.base) || "main";
const MAX_FIX_ROUNDS = (args && Number(args.maxFixRounds)) || 2;
const MAX_TASKS = 24;

if (!GOAL) {
  return {
    error:
      "No goal provided. Pass the feature requirement as args (string) or args.goal.",
  };
}

const HARD_RULES = `HARD RULES (non-negotiable, from CLAUDE.md):
- No AI/LLM attribution anywhere — no Co-Authored-By AI trailers, no "generated by" notes in code, comments, or commit messages.
- Conventional Commits for any commit (feat/fix/docs/chore/refactor/test/perf/ci/build/revert, optional scope).
- Match the surrounding code's style, naming, and comment density. Read neighbors before writing.`;

const PLAN_SCHEMA = {
  type: "object",
  additionalProperties: false,
  required: ["summary", "tasks"],
  properties: {
    summary: { type: "string", description: "One-paragraph approach" },
    tasks: {
      type: "array",
      description:
        "Disjoint, parallelizable units of work. File scopes MUST NOT overlap across tasks.",
      items: {
        type: "object",
        additionalProperties: false,
        required: ["id", "title", "files", "instructions", "acceptance"],
        properties: {
          id: { type: "string" },
          title: { type: "string" },
          files: {
            type: "array",
            items: { type: "string" },
            description: "Exact file paths/globs this task owns. No overlap.",
          },
          instructions: { type: "string" },
          acceptance: {
            type: "string",
            description: "Observable done criteria",
          },
        },
      },
    },
  },
};

const REVIEW_SCHEMA = {
  type: "object",
  additionalProperties: false,
  required: ["verdict", "findings"],
  properties: {
    verdict: { type: "string", enum: ["approve", "request-changes"] },
    findings: {
      type: "array",
      items: {
        type: "object",
        additionalProperties: false,
        required: ["severity", "file", "problem", "fix"],
        properties: {
          severity: {
            type: "string",
            enum: ["critical", "major", "minor", "nit"],
          },
          file: { type: "string" },
          problem: { type: "string" },
          fix: { type: "string" },
        },
      },
    },
  },
};

// ── Phase 1: Plan (Opus) ────────────────────────────────────────────────────
phase("Plan");
const plan = await agent(
  `You are a senior architect. Plan the implementation of this feature in the current repo:

GOAL: ${GOAL}

Read the codebase to ground the plan in real files and conventions. Decompose into the SMALLEST set of tasks whose file scopes DO NOT OVERLAP, so multiple implementers can work in parallel without conflicts. If two pieces of work must touch the same file, keep them in ONE task. Each task gets exact file paths it owns, precise instructions, and observable acceptance criteria.

${HARD_RULES}

Return structured output only.`,
  { label: "plan", phase: "Plan", schema: PLAN_SCHEMA, model: "opus" },
);

if (!plan || !plan.tasks || !plan.tasks.length) {
  return { error: "Planner produced no tasks.", plan };
}

log(`Plan: ${plan.tasks.length} disjoint tasks. ${plan.summary}`);

// Cap runaway plans — no silent truncation.
let tasks = plan.tasks;
if (tasks.length > MAX_TASKS) {
  log(
    `Plan returned ${tasks.length} tasks; capping to MAX_TASKS=${MAX_TASKS} (dropping ${tasks.length - MAX_TASKS}). Re-run with a narrower goal if the dropped tasks are needed.`,
  );
  tasks = tasks.slice(0, MAX_TASKS);
}

// Runtime overlap guard: disjointness is asserted by the planner LLM but not
// trusted. If two tasks claim the same file scope, parallel implementers would
// clobber each other in the shared working tree. Detect overlaps; on conflict
// fall back to sequential implementation so edits can't race.
// Normalize a scope entry: strip leading "./", collapse repeated slashes, drop
// trailing "/". Returns the directory prefix a scope "covers" — for a glob
// (anything containing * ? [ {) that's the literal directory before the first
// wildcard segment; for a plain dir (trailing "/") or path it's the path itself.
function normalizeScope(raw) {
  let s = String(raw)
    .trim()
    .replace(/^\.\//, "")
    .replace(/\/{2,}/g, "/");
  s = s.replace(/\/$/, "");
  return s;
}
// Directory prefix a scope governs, used for subtree containment checks.
function coveragePrefix(norm) {
  const segs = norm.split("/");
  const out = [];
  for (const seg of segs) {
    if (/[*?[\]{}]/.test(seg)) break; // first wildcard segment ends the literal prefix
    out.push(seg);
  }
  return out.join("/");
}
// True if scope `a` and scope `b` could touch the same file: one is the other,
// or one's coverage prefix is an ancestor (or equal) of the other's path.
function scopesCollide(a, b) {
  const na = normalizeScope(a);
  const nb = normalizeScope(b);
  if (na === nb) return true;
  const pa = coveragePrefix(na);
  const pb = coveragePrefix(nb);
  // A leading-wildcard scope (e.g. "**" or "*.ts" at root) yields an empty
  // coverage prefix yet actually covers the whole tree. Treat that as a
  // root-covering wildcard that collides with everything -- fail safe toward
  // sequential, never fail-open into unguarded parallel writes.
  if (pa === "" || pb === "") return true;
  const under = (parent, child) =>
    parent !== "" && (child === parent || child.startsWith(parent + "/"));
  // A glob/dir prefix covers anything beneath it; compare against the other's
  // full normalized path AND its prefix so dir-vs-file and glob-vs-file both hit.
  return under(pa, nb) || under(pb, na) || under(pa, pb) || under(pb, pa);
}
// Detect overlapping file scopes across tasks. Comparison is glob/prefix-aware
// and path-normalized; when in doubt it flags a collision (fail safe toward the
// cheap sequential fallback).
function scopeOverlaps(taskList) {
  const owned = []; // { file, id }
  const conflicts = [];
  for (const t of taskList) {
    for (const f of t.files || []) {
      const clash = owned.find(
        (o) => o.id !== t.id && scopesCollide(o.file, f),
      );
      if (clash) {
        conflicts.push(`"${f}" (tasks ${clash.id} and ${t.id})`);
      }
      owned.push({ file: f, id: t.id });
    }
  }
  return conflicts;
}
const overlaps = scopeOverlaps(tasks);
const runSequential = overlaps.length > 0;
if (runSequential) {
  log(
    `WARNING: overlapping file scopes detected — ${overlaps.join("; ")}. Running implementers SEQUENTIALLY to avoid clobbering in the shared working tree.`,
  );
}

// ── Phase 2: Implement (Sonnet) ─────────────────────────────────────────────
phase("Implement");
function implementPrompt(task) {
  return `You are a Sonnet implementer. Implement EXACTLY this task and nothing outside its file scope.

Task ${task.id}: ${task.title}
Files you own (do not edit anything else): ${task.files.join(", ")}
Instructions: ${task.instructions}
Acceptance: ${task.acceptance}

Overall feature context: ${GOAL}

${HARD_RULES}

Read the owned files and their neighbors first, then make focused edits. Do NOT commit — leave changes in the working tree. Report a concise summary of what you changed file-by-file. Return your summary as text.`;
}

const runOne = (task) =>
  agent(implementPrompt(task), {
    label: `impl:${task.id}`,
    phase: "Implement",
    model: "sonnet",
  }).then((summary) => (summary ? { task: task.id, summary } : null));

let implResults;
if (runSequential) {
  // Overlapping scopes: run one at a time so concurrent edits can't race.
  implResults = [];
  for (const task of tasks) {
    implResults.push(await runOne(task));
  }
} else {
  implResults = await parallel(tasks.map((task) => () => runOne(task)));
}

const built = implResults.filter((r) => r && r.summary);
log(`Implemented ${built.length}/${tasks.length} tasks.`);
const failed = tasks.length - built.length;
if (failed) log(`${failed} implementer(s) produced no output.`);

// Zero-build guard: if every implementer died, the working tree is unchanged.
// Don't burn the Opus review (and a Sonnet fix round) on an empty diff.
if (!built.length) {
  return {
    goal: GOAL,
    plan: { summary: plan.summary, taskCount: tasks.length },
    implemented: [],
    error: "All implementers produced no output; nothing to review.",
  };
}

// ── Phase 3/4: Review (Opus) → Fix (Sonnet) loop ────────────────────────────
let round = 0;
let lastReview = null;
while (round < MAX_FIX_ROUNDS) {
  round++;
  phase("Review");
  const review = await agent(
    `You are an adversarial Opus reviewer. Review the full working-tree diff implementing this feature:

GOAL: ${GOAL}

Implementers were told NOT to commit — all changes are uncommitted in the working tree. Review the working-tree state, not committed history: run \`git diff ${BASE}\` (tracked, unstaged changes), \`git diff --staged ${BASE}\` if anything is staged, and \`git status --porcelain\` to surface NEW UNTRACKED files; read any untracked new files directly since they won't appear in any diff. Do NOT rely on \`git diff ${BASE}...HEAD\` — this workflow forbids commits, so that range is empty. Then read the changed files in context. Check correctness, scope (nothing outside the planned files), regressions, security, and that the acceptance criteria are met. ${HARD_RULES}

Approve only if there are no critical/major findings. Otherwise request-changes with concrete file + fix per finding. Return structured output only.`,
    {
      label: `review:r${round}`,
      phase: "Review",
      schema: REVIEW_SCHEMA,
      model: "opus",
    },
  );
  lastReview = review;

  const blocking = ((review && review.findings) || []).filter((f) =>
    ["critical", "major"].includes(f.severity),
  );

  if (!review) {
    // A null review means the reviewer was skipped or died — NOT an approval.
    // Refute-by-default: never let a dead adversarial gate rubber-stamp the feature.
    log(
      `Review round ${round}: reviewer produced no output — adversarial gate did not run. Leaving for human review.`,
    );
    break;
  }

  if (review.verdict === "approve" && !blocking.length) {
    log(`Review round ${round}: approved.`);
    break;
  }

  log(`Review round ${round}: ${blocking.length} blocking findings — fixing.`);
  if (round === MAX_FIX_ROUNDS) {
    log(`Max fix rounds reached; leaving remaining findings for human review.`);
    break;
  }

  phase("Fix");
  const list = blocking
    .map(
      (f, i) =>
        `${i + 1}. [${f.severity}] ${f.file}: ${f.problem}\n   Fix: ${f.fix}`,
    )
    .join("\n");
  await agent(
    `You are a Sonnet fixer with Edit access. Resolve these review findings in the working tree WITHOUT degrading the feature or touching unrelated code:

${list}

${HARD_RULES}

Read each file fresh, apply targeted fixes, and report what you changed. Return text.`,
    { label: `fix:r${round}`, phase: "Fix", model: "sonnet" },
  );
}

return {
  goal: GOAL,
  plan: {
    summary: plan.summary,
    taskCount: tasks.length,
    droppedTasks: plan.tasks.length - tasks.length,
  },
  implemented: built,
  finalReview: lastReview,
  fixRounds: round,
};
