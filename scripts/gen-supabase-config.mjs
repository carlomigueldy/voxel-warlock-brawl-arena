#!/usr/bin/env node
// Generates src/supabase-config.js from environment variables at build/start time.
//
// Why: the game is a build-free static site, so the browser cannot read runtime
// env vars. This script bakes SUPABASE_URL / SUPABASE_ANON_KEY into a committed-shape
// JS module so both Vercel deploys (vercel-build) and local dev (prestart) can inject
// credentials without hardcoding them in git. The anon key is public-by-design.
//
// Sources, in order of precedence: real process env > a local .env file (KEY=VALUE).
// If credentials are absent it leaves placeholders so the app still boots (online
// features simply stay disabled) — the build never fails for missing creds.

import { readFileSync, writeFileSync, existsSync } from 'node:fs';
import { fileURLToPath } from 'node:url';
import { dirname, resolve } from 'node:path';

const __dirname = dirname(fileURLToPath(import.meta.url));
const root = resolve(__dirname, '..');
const outPath = resolve(root, 'src/supabase-config.js');
const envPath = resolve(root, '.env');

// Lightweight .env loader (no dependency). process.env wins over .env file values.
const fromEnvFile = {};
if (existsSync(envPath)) {
  for (const line of readFileSync(envPath, 'utf8').split('\n')) {
    const m = line.match(/^\s*([A-Z0-9_]+)\s*=\s*(.*)\s*$/);
    if (!m) continue;
    let val = m[2].trim();
    if ((val.startsWith('"') && val.endsWith('"')) || (val.startsWith("'") && val.endsWith("'"))) {
      val = val.slice(1, -1);
    }
    fromEnvFile[m[1]] = val;
  }
}

const url = process.env.SUPABASE_URL || fromEnvFile.SUPABASE_URL || '';
const anonKey = process.env.SUPABASE_ANON_KEY || fromEnvFile.SUPABASE_ANON_KEY || '';

const file = `// AUTO-GENERATED by scripts/gen-supabase-config.mjs — do not edit by hand.
// Populated from SUPABASE_URL / SUPABASE_ANON_KEY env vars (or a local .env).
// SUPABASE_ANON_KEY is intentionally public-by-design — safe to expose to the browser.
export const SUPABASE_URL = ${JSON.stringify(url)};
export const SUPABASE_ANON_KEY = ${JSON.stringify(anonKey)};
`;

writeFileSync(outPath, file);
console.log(
  url && anonKey
    ? `[gen-supabase-config] wrote src/supabase-config.js for ${url}`
    : '[gen-supabase-config] no SUPABASE_URL/SUPABASE_ANON_KEY found — wrote empty placeholders (online features disabled)'
);
